#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != 0)
	{
		if(*s == (char)c)
		{
			return (s);	
		}
		s++;
	}
	if (s == (char)c)
		return (s);
	else
		return (0);
}